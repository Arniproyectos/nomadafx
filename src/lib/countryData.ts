// Funci√≥n para calcular el poder adquisitivo
export const calculatePurchasingPower = (
  originIncome: number,
  destinationCost: number
): number => {
  return Math.round((originIncome / destinationCost) * 100) / 100;
};

// Funci√≥n para ordenar pa√≠ses por oportunidad
export const sortByOpportunity = (countries: CountryData[]): CountryData[] => {
  return [...countries].sort((a, b) => {
    // Combinar devaluaci√≥n alta + costo bajo = mejor oportunidad
    const scoreA = Math.abs(a.devaluationVsUSD) * 0.5 + (1000 - a.monthlyLivingCost) * 0.05;
    const scoreB = Math.abs(b.devaluationVsUSD) * 0.5 + (1000 - b.monthlyLivingCost) * 0.05;
    return scoreB - scoreA;
  });
};
